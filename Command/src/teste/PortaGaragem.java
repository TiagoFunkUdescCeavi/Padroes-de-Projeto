package teste;

public class PortaGaragem {

	public PortaGaragem() {
	}
	
	public void up() {
		System.out.println("Subindo a porta");
	}
	
	public void down() {
		System.out.println("Descendo a porta");
	}
	
	public void stop() {
		System.out.println("Parando a porta");
	}
	
	public void lightOn() {
		System.out.println("Ligando a luz da porta");
	}
	
	public void lightOff() {
		System.out.println("Desligando a luz da porta");
	}
}

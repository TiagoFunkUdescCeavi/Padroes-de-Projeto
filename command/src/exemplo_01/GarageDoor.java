package exemplo_01;

public class GarageDoor {

	public void up() {
		System.out.println( "Subindo a porta" );
	}
	
	public void down() {
		System.out.println( "Descendo a porta" );
	}
	
	public void stop() {
		System.out.println( "Parando a porta" );
	}
	
	public void on() {
		System.out.println( "Ligando a porta" );
	}
	
	public void off() {
		System.out.println( "Desligando a porta" );
	}
}
